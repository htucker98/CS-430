public class Pixel {
    public int red;
    public int green;
    public int blue;

    public Pixel(int r, int g, int b) {
        this.red = r;
        this.green = g;
        this.blue = b;
    }


    public void setColor(int r, int g, int b){
        this.red = r;
        this.green = g;
        this.blue = b;
    }
}

